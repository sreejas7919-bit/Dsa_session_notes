# Python Operators Example

a = 10
b = 5

# Arithmetic Operators
print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)

# Comparison Operators
print("a > b:", a > b)
print("a < b:", a < b)
print("a == b:", a == b)

# Logical Operators
print("a > 5 and b < 10:", a > 5 and b < 10)
print("a > 5 or b > 10:", a > 5 or b > 10)

# Assignment Operator
c = a + b
print("Value of c:", c)