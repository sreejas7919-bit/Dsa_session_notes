# Python Conditional Statements

age = 18

# if statement
if age >= 18:
    print("You are eligible to vote")

# if-else statement
number = 7

if number % 2 == 0:
    print("Even Number")
else:
    print("Odd Number")

# if-elif-else statement
mark = 85

if mark >= 90:
    print("Grade A+")
elif mark >= 75:
    print("Grade A")
elif mark >= 50:
    print("Grade B")
else:
    print("Fail")