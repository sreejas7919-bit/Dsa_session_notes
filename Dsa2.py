# Python Data Types Example

# Integer
age = 20

# Float
height = 5.6

# String
name = "Sree"

# Boolean
is_student = True

# List
colors = ["Red", "Blue", "Green"]

# Tuple
numbers = (1, 2, 3)

# Dictionary
student = {
    "name": "Sree",
    "age": 20
}

print("Integer:", age)
print("Float:", height)
print("String:", name)
print("Boolean:", is_student)
print("List:", colors)
print("Tuple:", numbers)
print("Dictionary:", student)